function animalsByIds(...ids) {
  if (ids.length === 0) {
    return [];
  }
  return animals.filter((cur, idx) => cur.id === ids[idx]);
}

function animalsOlderThan(animal, age) {
  return animals.find(cur => cur.name === animal).residents.every(cur => cur.age > age);
}

